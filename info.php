        <article id="principyvyroby">
          <h2 class="major">
            Principy Cirkulární ekonomiky<br />
            ve výrobě
          </h2>
		  <span class="image main"><img src="images/pic03.jpg" alt=""/></span>
		  <a href="http://obcevkruhu.cz/social/register.php" class="button primary small">Přihlásit se</a><p></p>
          <p>Principy oběhového hospodářství je nezbytné respektovat už na samém počátku životního cyklu výrobku, protože fáze návrhu i výrobní postupy mají dopad na získávání zdrojů, jejich využívání a vznik odpadu po celou dobu životnosti výrobku. Klíčová je zejména životnost výrobků, kdy lze dosáhnout delší doby životnosti či udržení výrobku ve fázi užití pomocí vhodného designu. Promyšlený design může vést také ke snadnější opravitelnosti, modernizaci či repasi výrobků, a může pomoci subjektům provádějícím recyklaci při rozmontování výrobků za účelem opětovného použití hodnotných surovin a součástí, kdy přímo napomáhá šetřit primární (nejen vzácné) zdroje  (Evropská komise 2015). </p><p>
			Ačkoliv je tato skutečnost dobře známa, nejsou v rámci trhu požadavky na vhodný design dostatečně reflektovány. Jedním z cílů Evropské unie je proto podle Akčního plánu pro oběhové hospodářství poskytování pobídek pro zlepšení designu výrobků prostřednictvím jejich inovací při zachování podmínek jednotného trhu a hospodářské soutěže (Evropská komise 2015). </p><p>
			S ohledem na vhodný ekodesign je pozornost věnována zejména elektrickým a elektronickým výrobkům, protože možnost jejich opravy  je výhodná pro spotřebitele, kdy zároveň dojde k udržení snadno opravitelných výrobků déle ve fázi užití,  a na konci jejich životnosti je klíčová jejich  snadná recyklovatelnost, zvláště s ohledem na cenné materiály, které jsou v těchto výrobcích zpravidla obsaženy (např. prvky vzácných zemin v elektronických zařízeních). Některé z požadavků na ekodesign elektrických a elektronických výrobků jsou již řešeny v rámci směrnice o ekodesignu 2009/125/ES, která se vztahuje na všechny výrobky spojené se spotřebou energie, a jejímž cílem je zlepšit účinnost a environmentální výkonnost výrobků spojených se spotřebou energie a klást důraz na aspekty oběhového hospodářství (Směrnice o ekodesignu 2009/125/ES). Zatímco však požadavky na ekodesign byly zaměřeny převážně na energetickou účinnost těchto výrobků, v budoucnu budou ze strany Evropské unie systematicky prozkoumávány otázky, jako je právě opravitelnost, trvanlivost, možnosti zdokonalení, recyklovatelnost či vymezení některých materiálů nebo látek, které tyto výrobky obsahují.  Podpora vhodného ekodesignu ze strany Evropské unie bude mimo jiné také zahrnovat stanovení výše finančního příspěvku, který hradí výrobci v rámci systémů rozšířené odpovědnosti výrobce, která bude stanovena na základě nákladů souvisejících s koncem životnosti jejich výrobků jako jeden z finančních nástrojů, jehož cílem je navrhování takových výrobků, které lze snadněji recyklovat nebo opětovně použít (Evropská komise 2015). </p><p>
			Stejně jako vhodný ekodesign jsou klíčové také výrobní postupy zachovávající principy oběhového hospodářství, protože neúčinné využívání jak primárních zdrojů, tak obnovitelných materiálů  při výrobě vede ke ztrátě obchodních příležitostí a vzniku značného množství odpadu. Z pohledu výroby je klíčový globální pohled se zaměřením na environmentální i sociální aspekty výroby, protože značná část celosvětové produkce probíhá ve třetích zemích. Evropská unie proto podporuje udržitelné získávání surovin na globální úrovni, a to zejména prostřednictvím politického dialogu, partnerství a své obchodní a rozvojové politiky (Evropská komise 2015). </p><p>
			Zásadní roli v této oblasti přirozeně zastává průmysl,  v oblasti využívání zdrojů, vzniku odpadu při výrobě a nakládání s ním však existují v jednotlivých průmyslových odvětvích značné rozdíly (Evropská komise 2015). S ohledem na principy cirkulární ekonomiky musí průmyslové podniky učinit konkrétní závazky v oblasti udržitelného získávání zdrojů a spolupráce v hodnotových řetězcích, Evropská unie proto bude intenzivně prosazovat osvědčené postupy při dosahování těchto cílů v celé řadě průmyslových odvětví prostřednictvím „referenčních dokumentů o nejlepších dostupných technikách“, které členské státy musí konzultovat při vydávání povolení pro průmyslová zařízení, a podporovat osvědčené postupy v oblasti odpadu z těžebního průmyslu, další podporou je např. vytvoření Evropského střediska excelence pro účinnost využívání zdrojů (Evropská komise 2014b). </p><p>
			Stranou podpory zavádění principů oběhového hospodářství při výrobních postupech ze strany Evropské unie nezůstávají ani malé a střední podniky, které mohou mimo jiné využívat obchodní příležitosti spojené s účinnějším využíváním zdrojů a dále podporu přístupu k inovativním technologiím či usnadnění procesu nahrazení nebezpečných chemických látek využívaných při výrobě (Evropská komise 2015). </p><p>
			Pro podniky, a to zejména pro malé a střední, by rovněž mohlo být přínosem zlepšení účinnosti a využívání systému Evropské unie pro environmentální řízení podniků a audit (EMAS) a pilotního projektu týkajícího se ověřování environmentálních technologií (ETV) (Evropská komise 2018), další možností je podpora inovativních průmyslových postupů jako je tzv. průmyslová symbióza, která umožňuje, aby odpad nebo vedlejší produkty jednoho odvětví mohly být použity jako vstupy pro jiné odvětví, kdy je základním předpokladem vyjasnění pravidel v oblasti jednotného výkladu pravidel pro využívání vedlejších produktů v členských státech Evropské unie (Evropská komise 2015). </p><p>
			Další možností optimalizace výrobních postupů ve vztahu k cirkulární ekonomice je podpora inovativních průmyslových postupů při opětovném využívání plynných odpadů a samozřejmě repase, které se běžně využívá v automobilovém průmyslu či u stavebních strojů, avšak v řadě průmyslových odvětví zatím není zažitou praxí (Evropská komise 2015). </p><p>
			Podpora těchto postupů ze strany Evropské unie jsou realizovány prostřednictvím programu pro financování výzkumu a inovací Horizont 2020 a prostřednictvím fondů politiky soudržnosti a dále zahrne pokyny týkající se osvědčených postupů v oblasti nakládání s odpady a účinného využívání zdrojů v průmyslových odvětvích do referenčních dokumentů o nejlepších dostupných technikách (Best available techniques Reference, BREF) a vydá pokyny a bude podporovat osvědčené postupy v oblasti odpadů z těžebního průmyslu (Evropská komise 2015).</p>			
