<h2 class="major"> <span style="color: var(--theme-color);">Pro občany:</span> Jak na to <br>(od námětu k realizaci) </h2>
<p></p>
<h4 class="major"> Aplikace námětu do prostředí obce </h4>
<p></p>

<p>Pro aplikaci námětu je nutné zhodnotit dostupné kapacity:</p>

<ul>
    <li>Personální: koho se námět dotýká, kdo je ochoten se zapojit do řešení</li>
    <li>Ekonomické: jaké jsou investiční a provozní nároky námětu</li>
    <li>Infrastrukturní: jaké jsou nároky na realizaci a provoz námětu z hlediska prostoru, 
    logistické dostupnosti, servisu </li>
</ul>

<p></p>

<p>Dále je potřeba posoudit, zda je námět pro obec jako takovou vhodný (z pohledu prostorových dispozic, velikosti obce, vstupů, 
využitelnosti případných výstupů, reálnosti navázání spolupráce s potřebnými partnery atd.)</p>

<p>Jako občan (kromě témat, která mohu realizovat sám, ve vlastní režii) především posuzuji, zda je v obci dostatek zájemců/partnerů, 
zda je prostředí dostatečně silné, zda jsou k dispozici kapacity a finance v potřebném rozsahu.</p>

<ul>
    <li>Vlastní řešení: zahrnuje pouze vlastní personální nasazení, prostředky a infrastrukturu.</li>
    <li>Komunální řešení (bez obce): námět lze řešit ve spojení více občanů, bez potřeby zapojení obce.</li>
    <li>Řešení ve spolupráci s obcí: k realizaci námětu je potřeba zapojení obce v důsledku např. záboru veřejného prostranství 
    nebo využití obecního pozemku.</li>
    <li>Obecní řešení: námět lze řešit pouze z pozice obce.</li>
</ul>

<p></p>

<p>Jako třetí osoba (místní instituce – školy, spolky, zájmová sdružení apod.) vycházím z potřeb a možností komunity 
a možnosti blízké spolupráce s obcí.</p>

<ul>

    <li>Vlastní komunitní řešení: k realizaci postačují síly členů komunity a její finanční i hmotné prostředky.</li>
    <li>Řešení s příspěvkem obce: k realizaci námětu je třeba finančního, hmotného nebo organizačního přispění obce.</li>
    <li>Řešení s příspěvkem jiné instituce: k realizaci námětu je třeba prostředků ze zdroje mimo komunitu a obec (ze státních 
    nebo krajských prostředků apod.). Možnost spolupráce s obcí na žádání o prostředky. </li>
    <li>Řešení se zapojením občanů: k realizaci je nutné zapojit a motivovat občany.</li>

</ul>

<p>Jako místní podnikatel vycházím z potřeb komunity a obce, se zohledněním vlastního přínosu a možností.</p>

<p></p>

<h4 class="major">Příprava konkrétního záměru</h4>

<p>Věnujte se pozorně těmto krokům:</p>

<ul>
    <li>Definice záměru a cíle: proč je vhodné záměr realizovat, čemu přispěje. Jaký je jeho cíl a je cíl měřitelný 
    (např. snížením produkce nevyužitelného odpadu)? Cíle záměru je vhodné stanovit tak, aby bylo měřitelné jejich dosažení.</li>
    <li>Mezi popisem stávajícího a zamýšleného stavu musí být zřejmý pozitivní rozdíl.</li>
    <li>Definice cílové skupiny a dotčených subjektů: pro koho je námět určen, kdo se na něm může nebo musí podílet, koho se dotkne.
     Pro koho bude mít pozitivní a případné negativní dopady. Vhodné je definovat skupiny v co nejširším měřítku, zejména v případě 
     potenciálně negativně dotčených subjektů, včetně návrhu eliminačních nebo kompenzačních opatření.</li>
    <li>Definice potřebných ekonomických prostředků: jaké jsou realizační a provozní náklady záměru, kde je možné tyto prostředky získat. 
    Tento krok zahrnuje zhodnocení vlastních možností (občana, komunity, obce, podnikatele) a případné hledání dalších možností 
    financování – veřejné podpory, veřejné lokální sbírky apod.</li>
    <li>Definice potřeb z hlediska infrastruktury: jaký prostor a infrastruktura jsou nezbytné k realizaci. Je možné záměr realizovat 
    kdekoliv, na několika nebo pouze na jednom konkrétním místě. Jaké místo je ideální a kdo je jeho vlastníkem. Kdo vlastní 
    a je ochoten poskytnout místo nebo infrastrukturu a za jakých podmínek. </li>
</ul>

<p>Vhodné je zpracování alespoň základní SWOT analýzy projektu, která obsahuje vyhodnocení silných a slabých stránek záměru 
a z něho vyplývajících příležitostí a hrozeb.</p>

<p>Velkým přínosem může být nalezení podobných příkladů (realizací) a komunikace s jejich realizátory. Tento krok pomůže 
s optimalizací záměru, včetně včasného snížení rizik.</p>

<p></p>

<h4 class="major">Hodnocení záměru a rozhodnutí o něm</h4>

<p>Záměr je potřeba zhodnotit z několika aspektů:</p>

<ul>
    <li>environmentální dopady: jaké bude mít realizace záměru důsledky z hlediska zvyšování využití odpadů, snižování emitované dopravy apod., a především z hlediska možných negativních vlivů – emisí škodlivin, hluku, prachu, zápachu nebo vlivu na estetický ráz.</li>
    <li>ekonomické dopady – jaký bude mít realizace a provoz záměru vliv na rozpočet komunity / obce / místní organizace. Jsou tyto náklady účelné, nebo je možné je využít efektivněji?</li>
    <li>dlouhodobá udržitelnost – co je nezbytné k dlouhodobému zajištění záměru z hlediska všech výše uvedených aspektů – personální a ekonomické náročnosti, environmentálních dopadů atd.</li>
</ul>

<p>Jako občan nebo místní podnikatel mám následující možnosti:</p>

<ul>
    <li>o vlastním řešení rozhoduji sám</li>
    <li>o komunálním řešení rozhoduje komunita – kdo se chce zapojit a zda je počet zapojených dostatečný,</li>
    <li>v případě řešení ve spolupráci s obcí, nebo přímo pouze obecního řešení je nutné předložení záměru zástupci obce, 
    který je představí obecnímu zastupitelstvu, nebo zastupitelstvu přímo, následuje proces popsaný 
    <span style="color:red;">v kapitole 2.4 metodiky</span></li>
</ul>

<p>Jako třetí osoba (místní instituce – školy, spolky, zájmová sdružení apod.) postupuji téměř identicky jako v případě občana:</p>

<ul>
    <li>o vlastním řešení rozhoduje samotná organizace, stejně jako o podání žádosti o pomoc jiné instituci, nebo o zapojení občanů do realizace záměru,</li>
    <li>v případě řešení s příspěvkem obce, případně s pomocí obce v žádosti směřované jiné instituci, je nutné předložení záměru zástupci obce, 
    který je představí obecnímu zastupitelstvu,</li>
    <li>v případě řešení se zapojením občanů je nutné jejich zapojení a motivace, k čemuž lze využít komunikaci prostřednictvím obce 
    či vlastní aktivitu (shromáždění občanů, obecní akce)</li>
</ul>

<p></p>

<h4 class="major">Realizace záměru a zapojení dalších subjektů</h4>

<p>V některých případech je vhodné rozčlenit záměr do několika etap z hlediska kapacity a rozsahu, a to z důvodu snížení náročnosti realizace, 
ale i získávání průběžné zpětné vazby.</p>
<p>Během realizace je nutné zejména přehledné a efektivní řízení projektu, sledování souladu jeho realizace s projektovou dokumentací 
a plnění dílčích cílů.</p>
<p>V případě realizace projektu s veřejnou podporou je třeba dodržet parametry obdržené podpory, vyplývající z rozhodnutí/smlouvě 
o dotaci/grantu, a to se zvláštním důrazem na veřejné zakázky a jejich zadávání.</p>
<p>Obecně lze konstatovat, že během realizace je zapotřebí dohlížet na plnění projektové dokumentace, harmonogramu a rozpočtu projektu, 
bezpečnost práce, profesionalitu řešení, environmentální a další aspekty. Je důležité zajistit tento monitoring zodpovědnou 
a způsobilou osobou.</p>

<p></p>

<h4 class="major">Monitoring a údržba realizovaného záměru</h4>

<p>Jde o sledování stavu záměru z hlediska samotné existence, plánovaných dopadů, parametrů a výsledků, v návaznosti na definované cíle, 
včetně vyhodnocování zpětné vazby. V rámci monitoringu je důležité zejména dohlížet na dlouhodobou udržitelnost a ekonomickou 
i environmentální efektivitu a v případě potřeby činit nutná opatření (údržba, změna provozu, upgrade dle aktuálních podmínek apod.). 
Pro monitoring je vhodné dlouhodobě stanovit zodpovědnou osobu.</p>

